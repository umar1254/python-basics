#advanced strings

message = 'Hello,world'

print(message[0]) #starts at the first character
print(message[1]) #starts at the second character

print(message[-1]) #starts at the last character


print(len(message))

print(message.strip())  #remove white space
print(message.lower())  #all lower case
print(message.split())   #split the string into a list based on the comma